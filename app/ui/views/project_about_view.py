"""Стартовая страница: о проекте, цель, статус, сценарий использования."""

from __future__ import annotations

import pandas as pd
import streamlit as st

from app.services.status_service import get_project_overview
from app.ui.demo_components import page_title
from app.ui.russian_ui import METRIC_LABELS_RU, format_artifacts_status_ru
from app.ui.utils import get_resolved_config


def render() -> None:
    page_title("Исследовательская система: риск поездки в каршеринге")

    st.markdown(
        """
### Тема исследования
**Разработка нейросетевой модели классификации для задач каршеринга.**

### Предметная область
Каршеринг — краткосрочная аренда автомобиля через приложение. Оператору важно заранее оценивать **риск проблемной поездки**
(инцидент, нарушение, мошенничество, сильный негатив), чтобы распределять внимание поддержки и антифрода.

### Цель разработки программного продукта
Показать полный цикл прикладного ML-проекта: данные → подготовка → обучение нейросети → оценка качества → прогноз —
в виде **понятного русскоязычного веб-приложения**, пригодного для демонстрации на защите диплома.

### Что реализовано в системе
- Работа с **демонстрационными** или **загруженными** табличными данными (CSV).  
- **Многослойный перцептрон (MLP)** в PyTorch для бинарной классификации.  
- **Предобработка** признаков (пропуски, масштабирование, one-hot) с фиксацией в артефактах.  
- **Оценка качества** и **одиночный / пакетный прогноз** через интерфейс.  
        """
    )

    cfg = get_resolved_config()
    ov = get_project_overview(cfg)

    st.subheader("Текущий статус системы")
    c1, c2, c3 = st.columns(3)
    c1.metric(
        "Модель обучена",
        "да" if ov["model_trained"] else "нет",
        help="Проверяются веса модели, препроцессор и дамп конфигурации обучения.",
    )
    c2.metric(
        "Исходные данные по пути из конфигурации",
        "файл найден" if ov["raw_dataset_exists"] else "не найден",
    )
    trained_at = ov.get("trained_at_utc")
    c3.metric(
        "Последнее обучение (UTC)",
        str(trained_at) if trained_at else "—",
    )

    st.subheader("Артефакты (контрольный список)")
    rows = format_artifacts_status_ru(ov["artifacts"])
    st.dataframe(
        pd.DataFrame(rows, columns=["Артефакт", "Статус"]),
        use_container_width=True,
        hide_index=True,
    )

    tm = ov.get("test_metrics")
    if tm:
        st.subheader("Ключевые метрики на тесте (последнее обучение)")
        st.caption("Краткая сводка; подробная интерпретация — в разделе «Результаты и метрики».")
        cols = st.columns(5)
        keys = ["accuracy", "precision", "recall", "f1", "roc_auc"]
        for col, key in zip(cols, keys):
            label, help_txt = METRIC_LABELS_RU.get(key, (key, ""))
            val = tm.get(key)
            if val is None:
                col.metric(label.split("(")[0].strip(), "—", help=help_txt)
            else:
                col.metric(label.split("(")[0].strip(), f"{float(val):.3f}", help=help_txt)

    st.subheader("Как пользоваться системой")
    st.markdown(
        """
1. **Постановка задачи** — уточните формулировку исследования и классы.  
2. **Данные** — сгенерируйте демо-набор или загрузите CSV; изучите признаки и баланс классов.  
3. **Предобработка данных** — посмотрите, как сырьё превращается в вход нейросети.  
4. **Обучение модели** — при необходимости запустите обучение и проследите за потерями.  
5. **Результаты и метрики** — оцените качество и ошибки первого/второго рода.  
6. **Демонстрация прогнозирования** — готовые сценарии и ручной ввод; смотрите таблицу результатов внизу экрана.  
7. **Практическая ценность** — сформулируйте для комиссии прикладной смысл и ограничения.  
        """
    )

