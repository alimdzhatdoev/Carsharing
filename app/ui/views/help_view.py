"""Справка и пошаговая инструкция для нового пользователя."""

from __future__ import annotations

import streamlit as st

from app.ui.demo_components import page_title


def render() -> None:
    page_title("Справка и инструкция")

    st.subheader("Как открыть систему")
    st.markdown(
        """
Из **корня репозитория** (где лежат папки `app/`, `configs/`, `data/`):

```bash
pip install -r requirements.txt
streamlit run app/ui/streamlit_app.py
```

Либо: `python scripts/launch_ui.py`  

В браузере откроется адрес вида **http://127.0.0.1:8501** — это и есть интерфейс дипломного проекта.
        """
    )

    st.subheader("Как пройти демонстрацию (рекомендуемый путь)")
    st.markdown(
        """
1. **О проекте** — статус модели и артефактов.  
2. **Постановка задачи** — формулировка задачи.  
3. **Данные** — сгенерировать демо-набор или загрузить CSV.  
4. **Предобработка данных** — шаги пайплайна.  
5. **Обучение модели** — запуск обучения и графики.  
6. **Результаты и метрики** — при необходимости «Пересчитать метрики».  
7. **Демонстрация прогнозирования** — готовые сценарии и ручной ввод; таблица результатов внизу экрана.  
8. **Практическая ценность** — выводы и ограничения.  
        """
    )

    st.subheader("Как загрузить свои данные")
    st.write(
        "В разделе **«Данные»** используйте загрузчик CSV. Файл должен содержать колонки по контракту проекта "
        "(см. `docs/03_data_description.md`), включая **`target_class`** для обучения. "
        "После сохранения файл появится в `data/raw/` и будет доступен в списке для обучения."
    )

    st.subheader("Что делать, если модель «не обучена»")
    st.write(
        "Раздел прогнозирования станет доступен после успешного обучения: веса в `artifacts/models/`, "
        "препроцессор в `artifacts/encoders/` и конфиг обучения. Сначала пройдите **«Обучение модели»**."
    )

    st.subheader("Где лежат артефакты")
    st.markdown(
        """
- **Модель и конфиг обучения:** `artifacts/models/`  
- **Препроцессор:** `artifacts/encoders/`  
- **Метрики и история эпох:** `artifacts/metrics/`  
- **Отчёты и матрица ошибок:** `artifacts/reports/`  
        """
    )

    st.subheader("API и Docker (кратко)")
    st.markdown(
        """
- **FastAPI:** `uvicorn app.api.main:app --host 0.0.0.0 --port 8000` — см. `README.md` и `docs/06_api_usage.md`.  
- **Docker (интерфейс):** `docker compose up ui` — порт **8501**.  
        """
    )
