"""Постановка задачи — текст для защиты."""

from __future__ import annotations

import streamlit as st

from app.ui.demo_components import page_title


def render() -> None:
    page_title("Постановка задачи")

    st.markdown("### Какая задача решается")
    st.write(
        "Решается задача **бинарной классификации**: по вектору признаков, описывающих пользователя, поездку, "
        "автомобиль и контекст (погода, трафик, зона и т.д.), нужно отнести поездку к одному из двух классов — "
        "**нормальная** (0) или **потенциально проблемная** (1). "
        "Под «проблемной» понимается повышенный риск инцидента, нарушения правил, мошенничества или сильного негатива для сервиса."
    )

    st.markdown("### Почему это важно для каршеринга")
    st.write(
        "Оператор обрабатывает тысячи поездок; полная ручная проверка невозможна. **Скоринг риска** позволяет "
        "приоритизировать выборки для модерации, антифрода и поддержки, снижая затраты и реагируя быстрее на опасные сценарии."
    )

    st.markdown("### Объект классификации")
    st.write(
        "**Объект** — одна завершённая (или почти завершённая) поездка каршеринга, представленная строкой таблицы признаков."
    )

    st.markdown("### Классы, которые предсказывает модель")
    st.markdown(
        """
| Класс | Смысл |
|-------|--------|
| **0** | Поездка выглядит типичной с точки зрения доступных признаков; явных сигналов повышенного риска нет. |
| **1** | Повышенный риск проблемной поездки; в продуктивной системе это сигнал для **дополнительной проверки**, а не автоматического наказания. |
        """
    )

    st.markdown("### Почему выбрана классификация")
    st.write(
        "Для операционных решений часто нужен **дискретный сигнал** «обратить внимание / не обращать» с настраиваемым порогом. "
        "Регрессия «степени риска» возможна как развитие, но классификация с вероятностью проще интерпретируется и согласуется с пороговой политикой."
    )

    st.markdown("### Вход и выход модели")
    st.write(
        "**На вход** подаются только **числовые и категориальные признаки** поездки (идентификаторы пользователя/поездки/машины в модель не входят). "
        "**На выходе** — **вероятность** класса 1 и итоговый класс после сравнения с порогом (настраивается при обучении/инференсе)."
    )

    st.markdown("### Зачем это на практике")
    st.write(
        "Модель помогает **ранжировать** и **фильтровать** поток поездок для последующих **человеческих** или **правиловых** решений, "
        "а не заменять их полностью."
    )

    st.divider()
    st.subheader("Цель работы")
    st.write(
        "Разработать и программно реализовать **нейросетевую модель** (MLP) для бинарной классификации риска поездки в каршеринге "
        "и встроить её в **демонстрационное приложение** с пояснением этапов исследования."
    )

    st.subheader("Задачи работы")
    st.markdown(
        """
1. Проанализировать предметную область и сформировать набор признаков и целевую переменную (в демо — синтетическая).  
2. Реализовать загрузку, проверку и **предобработку** данных.  
3. Спроектировать и **обучить** многослойный перцептрон в PyTorch.  
4. **Оценить качество** на отложенной выборке и интерпретировать метрики.  
5. Реализовать **прогноз** (одиночный и пакетный) и **веб-интерфейс** для пошаговой демонстрации.  
        """
    )

    st.subheader("Объект исследования")
    st.write("Процессы обработки данных и **нейросетевые методы бинарной классификации** в приложении к задачам каршеринга.")

    st.subheader("Предмет исследования")
    st.write(
        "**Модели, методы и программная реализация** пайплайна: от сырых табличных данных до обученной модели и прогноза."
    )

    st.subheader("Практическая значимость")
    st.write(
        "Результаты показывают, как типовой **production-oriented** ML-цикл можно оформить как **исследовательский программный продукт** "
        "и адаптировать к реальным корпоративным данным при наличии разметки и инфраструктуры."
    )
