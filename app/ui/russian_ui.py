def format_artifacts_status_ru(artifacts: dict[str, bool]) -> list[tuple[str, str]]:
    rows: list[tuple[str, str]] = []
    for key, ok in artifacts.items():
        label = ARTIFACT_LABELS_RU.get(key, key)
        rows.append((label, "найден" if ok else "отсутствует"))
    return rows
